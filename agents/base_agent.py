# Visual-only delta field vocabulary (24 fields + "other")
# ---------------------------------------------------------------------------

ALLOWED_DELTA_FIELDS: Tuple[str, ...] = (
    # Leaf features (8) — the doc's CoT spends most time on these
    "leaf_lesion_shape",     # circular / angular / irregular / elongated
    "leaf_lesion_color",     # specific color description (palette / hex / common-name)
    "leaf_lesion_texture",   # sunken / raised / smooth / fuzzy / leathery
    "leaf_chlorosis",        # interveinal / marginal / generalized / mosaic
    "leaf_necrosis",         # tip / margin / spot / extending-from-vein
    "leaf_curl",             # curling / blistering / puckering / cupping
    "leaf_vein_pattern",     # vein clearing / vein necrosis / vein-aligned spots
    "leaf_geometry",         # leaf shape / length-to-width / serration / lobation
    # Stem features (4) — decisive in look-alikes like SDS vs BSR
    "stem_lesion",           # cankers / girdling / sunken-raised on stem surface
    "stem_pith",             # internal pith color when split (white / brown / cardboard-like)
    "stem_surface",          # galls / blisters / scabs / exudates on outer stem
    "stem_discoloration",    # vascular streaking / browning visible through bark
    # Root + crown (2)
    "root_visible",          # rot / color / cysts / blue fungal masses on visible roots
    "crown_collar",          # crown / soil-line lesions / girdling at collar
    # Reproductive (2)
    "flower",                # flower blight / distortion / discoloration
    "fruit",                 # fruit lesions / mummification / scab
    # Signs vs symptoms (1)
    "sporulation",           # mycelium / spore masses / fruiting bodies / bacterial ooze
    # Whole-plant patterns (3) — top-down spatial reasoning
    "wilting",               # whole-plant / one-side / hemispheric / branch-localized
    "defoliation",           # leaf drop pattern + whether petioles remain attached
    "spatial_pattern",       # within-canopy or within-field distribution
    # Diagnostic cross-cutters (4)
    "concentric_pattern",    # target spots / halos / concentric rings
    "color_palette",         # dominant + secondary lesion-area colors (palette encoder)
    "look_alikes_visual",    # CoT decision-graph against known confusable diseases
    "severity_visible",      # fraction of organ affected (low / medium / high) by visible area
    "other",
)

CONFIDENCE_LEVELS = ("high", "medium", "low")


