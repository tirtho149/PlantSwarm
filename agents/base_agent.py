"""
agents/base_agent.py
====================
Base class for the Qwen visual-symptom swarm.

The swarm focuses **exclusively on visual symptoms** — the canonical KB
already carries pathogen / type / treatments / cultural-control info
generated by Claude in Phase 0; the Qwen swarm's only job is to look at
ONE field photograph and produce image-grounded deltas against the
canonical visual_symptoms block.

The swarm is decomposed into **24 single-feature specialists** + one
``VisualDiagnosisAgent`` consolidator. Each specialist owns ONE delta
field and asks a single laser-focused question about the photograph,
following the chain-of-thought pattern documented in
``Look alike Diseases, weeds and Insect COT with decision graph.docx``
(per-step decisive forks, e.g. "Is the lower stem pith white or brown?
white -> SDS, brown -> BSR").

Delta schema (matches pathome.RegionalDelta):
    {
      "field":          str,   # one of ALLOWED_DELTA_FIELDS
      "canonical_says": str,   # short quote, or "(not specified)"
      "image_shows":    str,   # what the photo shows that adds / contradicts
      "image_quote":    str,   # one-sentence visual evidence
    }
"""

