"""
scripts/viz/observe_eval.py
============================
Evaluation visualizations from results/observe_eval.json.

OBSERVE is now a KB-augmented OOD classifier (SigLIP-2 + LoRA), so the
eval JSON has the shape produced by scripts/evaluate_observe.py:

  {
    "crop": "Tomato",
    "evals": {
      "plantvillage": {"n_samples":..., "top1_accuracy":..., "top5_accuracy":...,
                       "macro_f1":..., "per_class": {label: {...}}},
      "plantwild":    {...},
    }
  }

We emit one summary table per dataset present plus a per-class bar
chart (per dataset). KB-known vs zero-shot synthesised classes are
shaded differently in the bar chart.

Outputs:
  results/figures/observe_eval_<dataset>.png
  plantswarm/latex/auto_observe_eval.tex
"""

from __future__ import annotations

import argparse
import json
import os
import sys
from pathlib import Path

ROOT = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
sys.path.insert(0, ROOT)

from scripts.viz._common import (
    ensure_dirs, fig_path, figure_includegraphics, get_mpl,
    have_matplotlib, latex_escape, write_tex,
)


def parse_args():
    p = argparse.ArgumentParser(description=__doc__.split("\n")[1])
    p.add_argument("--eval", required=True,
                   help="Path to results/observe_eval.json")
    p.add_argument("--name", default="observe_eval")
    return p.parse_args()


def _plot_per_class(plt, per_class: dict, out_png: Path, title: str) -> bool:
    if not per_class:
        return False
    items = sorted(per_class.items())
    labels = [k.split("::", 1)[-1] for k, _ in items]
    accs   = [float(v.get("accuracy", 0.0)) for _, v in items]
    in_kb  = [bool(v.get("in_kb", True))    for _, v in items]
    colors = ["#1E88E5" if k else "#FB8C00" for k in in_kb]
    fig, ax = plt.subplots(figsize=(8, max(3, 0.32 * len(items) + 1)))
    bars = ax.barh(labels, accs, color=colors)
    for bar, v in zip(bars, accs):
        ax.text(min(bar.get_width() + 0.01, 0.98),
                bar.get_y() + bar.get_height() / 2,
                f"{v:.2f}", va="center", fontsize=8)
    ax.set_xlim(0, 1.0)
    ax.set_xlabel("Top-1 accuracy")
    ax.set_title(title)
    ax.grid(True, axis="x", alpha=0.3)
    # legend
    from matplotlib.patches import Patch
    handles = [
        Patch(facecolor="#1E88E5", label="KB-known"),
        Patch(facecolor="#FB8C00", label="zero-shot"),
    ]
    ax.legend(handles=handles, loc="lower right", fontsize=8)
    fig.savefig(out_png, dpi=150, bbox_inches="tight")
    plt.close(fig)
    return True


def _dataset_table(name: str, ev: dict) -> list:
    lines: list = []
    lines.append(r"\begin{table}[t]")
    lines.append(r"  \centering\small")
    lines.append(r"  \begin{tabular}{lr}")
    lines.append(r"    \toprule")
    lines.append(r"    Metric & Value \\")
    lines.append(r"    \midrule")
    lines.append(f"    Samples              & {ev.get('n_samples', 0)} \\\\")
    lines.append(f"    Top-1 accuracy       & {ev.get('top1_accuracy', 0):.3f} \\\\")
    lines.append(f"    Top-5 accuracy       & {ev.get('top5_accuracy', 0):.3f} \\\\")
    lines.append(f"    Macro F1             & {ev.get('macro_f1', 0):.3f} \\\\")
    lines.append(r"    \bottomrule")
    lines.append(r"  \end{tabular}")
    lines.append(rf"  \caption{{OBSERVE eval on \texttt{{{latex_escape(name)}}}.}}")
    lines.append(rf"  \label{{tab:observe_eval_{name}}}")
    lines.append(r"\end{table}")
    lines.append("")
    return lines


def _per_class_table(name: str, ev: dict) -> list:
    per_class = ev.get("per_class") or {}
    if not per_class:
        return []
    lines: list = []
    lines.append(r"\begin{table}[t]")
    lines.append(r"  \centering\small")
    lines.append(r"  \begin{tabular}{lrrl}")
    lines.append(r"    \toprule")
    lines.append(r"    Class & Support & Top-1 & Source \\")
    lines.append(r"    \midrule")
    for lbl, pc in sorted(per_class.items()):
        src = "KB" if pc.get("in_kb", True) else "zero-shot"
        disp = latex_escape(lbl.split("::", 1)[-1])
        lines.append(
            f"    {disp} & {pc.get('support',0)} & {pc.get('accuracy',0):.3f} & {src} \\\\"
        )
    lines.append(r"    \bottomrule")
    lines.append(r"  \end{tabular}")
    lines.append(rf"  \caption{{Per-class OBSERVE accuracy on \texttt{{{latex_escape(name)}}}.}}")
    lines.append(rf"  \label{{tab:observe_eval_perclass_{name}}}")
    lines.append(r"\end{table}")
    lines.append("")
    return lines


def _build_tex(name: str, eval_data: dict, figures_written: list) -> str:
    lines: list = []
    lines.append(r"% Auto-generated by scripts/viz/observe_eval.py — do not edit by hand.")
    lines.append("")
    crop = eval_data.get("crop", "?")
    lines.append(rf"% OBSERVE evaluation (crop={crop}).")
    lines.append("")
    evals = eval_data.get("evals") or {}
    for tag, ev in evals.items():
        if not isinstance(ev, dict):
            continue
        lines += _dataset_table(tag, ev)
        lines += _per_class_table(tag, ev)
    for fig_name, caption, label in figures_written:
        lines.append(figure_includegraphics(fig_name, caption, label))
        lines.append("")
    return "\n".join(lines)


def _print_summary(eval_data: dict) -> None:
    crop = eval_data.get("crop", "?")
    print(f"=== OBSERVE eval (crop={crop}) ===")
    for tag, ev in (eval_data.get("evals") or {}).items():
        if not isinstance(ev, dict):
            continue
        print(f"  [{tag}]")
        print(f"    n_samples : {ev.get('n_samples')}")
        print(f"    top1      : {ev.get('top1_accuracy', 0):.3f}")
        print(f"    top5      : {ev.get('top5_accuracy', 0):.3f}")
        print(f"    macro_f1  : {ev.get('macro_f1',     0):.3f}")


def main() -> None:
    args = parse_args()
    ensure_dirs()
    if not Path(args.eval).is_file():
        raise SystemExit(f"eval JSON not found: {args.eval}")
    with open(args.eval) as f:
        eval_data = json.load(f)

    _print_summary(eval_data)

    figures_written: list = []
    if have_matplotlib():
        _, plt = get_mpl()
        for tag, ev in (eval_data.get("evals") or {}).items():
            if not isinstance(ev, dict):
                continue
            fig_tag = f"observe_eval_{tag}"
            out_png = fig_path(fig_tag)
            if _plot_per_class(plt, ev.get("per_class") or {}, out_png,
                               title=f"OBSERVE per-class top-1 on {tag}"):
                print(f"  wrote {out_png}")
                figures_written.append((
                    fig_tag,
                    f"OBSERVE per-class top-1 accuracy on {tag}.",
                    f"fig:observe_eval_{tag}",
                ))
    else:
        print("  matplotlib not installed — table-only output")

    tex_out = write_tex(args.name, _build_tex(args.name, eval_data, figures_written))
    print(f"  wrote {tex_out}")


if __name__ == "__main__":
    main()
